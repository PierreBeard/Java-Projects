package com.example.planninginsa;

public class id {

	private String url;
	private String nom;
	
	public id(String url, String nom) {
		super();
		this.url = url;
		this.nom = nom;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}
	
	

}
